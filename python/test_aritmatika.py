x = 3
x += 2
y = 2

# membuat bilangan jadi negatif (~)
print(f'{x} + -{y} = ', x + ~y) # Penjumlahan
print(f'{x} - {y} = ', x - y) # Pengurangan
print(f'{x} * {y} = ', x * y) # Perkalian
print(f'{x} / {y} = ', x / y) # Pembagian Pecahan
print(f'{x} // {y} = ', x // y) # Pembagaina Bulat
print(f'{x} % {y} = ', x % y) # Modulus sisa bagi
print(f'{x} ** {y} = ', x ** y) # Pangkat