# # 1. Buatlah codingan yang menampilkan ilangan dengan dua buah penjumlahan, pembagian dam hasil bagi dua
# #    buah variabel dengan nilai {x} dan 7. Dengan ketentuan variabel tersebut ditampilkan pada layar monitor.

x = 10
y = 2

print(f'Nilai x = {x}, Nilai y = {y}\n')

print(f'Hasil {x} + {y} = ',x + y) # penjumlahan
print(f'Hasil {x} - {y} = ',x - y) # pengurangan
print(f'Hasil {x} * {y} =', x * y) # perkalian
print(f'Hasil {x} / {y} =', x / y) # pembagian
print(f'Hasil {x} // {y} =', x // y) # pembagian floor di bulatkan ke atas lebih dari 5 atau kebawah kurang dari 5
print(f'Hasil {x} % {y} = ', x % y) # sisa bagi modulus
print(f'Hasil {x} ** {y} = ', x ** y) # pemangkatan
print(f'Hasil {x} & {y} = ', x & y) # AND
print(f'Hasil {x} | {y} = ', x | y) # OR
print(f'Hasil {x} ^ {y} = ', x ^ y) # XOR
print(f'Hasil ~{x} = ', ~x) # NOT
print(f'Hasil {x} << {y} = ', x << y) # left shift
print(f'Hasil {x} >> {y} = ', x >> y) # right shift