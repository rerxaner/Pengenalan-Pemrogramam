ipk = float(input('Masukan IPK Anda: '))

if ipk >= 3.60 and ipk <= 4.00:
    print("Nilai Anda sangat bagus sekali")
elif ipk >= 3.00 and ipk <= 3.59:
    print("Sangat Memuaskan")
elif ipk >= 2.50 and ipk <= 2.99:
    print("Memuaskan")
else:
    print("Belajar yang rajin ya!!")